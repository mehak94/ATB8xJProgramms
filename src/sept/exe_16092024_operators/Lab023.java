package sept.exe_16092024_operators;

public class Lab023 {
    public static void main(String[] args) {
        //arithmetic operators
        //+,-,*,/
        // they are binary operators as they require two operands
        //% - modulus operator , it gives us remainder
        int a = 5;
        int b = 8;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);


    }
}
