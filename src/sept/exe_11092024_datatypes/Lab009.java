package sept.exe_11092024_datatypes;

public class Lab009 {
    public static void main(String[] args) {
        //variable_name(Identifiers) - Rules we have to follow.
        // 1) Identifier can contain alphabets, Digits and two special symbols( $,_).
        // 2) First character of an identifier must be an alphabet or $ or _.
        // 3) Keywords or reserved words cannot be used as identifiers.
        // boolean 123 = true, not possible
        boolean $ = true;
        boolean _123 = true;
        boolean Mehak = true;
        System.out.println(_123);
        // EXIT CODE 0 MEANS SUCCESS NO ERROR.
        System.out.println(Mehak);
    }
}
