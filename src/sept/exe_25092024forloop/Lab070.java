package sept.exe_25092024forloop;

public class Lab070 {
    public static void main(String[] args) {
// For Loop
        // for is keyword
        //syntax:- for(A:B:C){}
        // A - Initialization , B - Condition for termination , C - Increment and Decrementation - updation
        for( int i = 1 ; i <= 10 ; i++){
            System.out.println(i);
        }
    }
}
